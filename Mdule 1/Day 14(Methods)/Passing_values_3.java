class Passing_values_3{
	static void Addition(int a,int b){	
		int sum =a+b;
		System.out.println(a+" + "+b+" = "+sum);

	}
		static void Substraction(int a,int b){	
		int sub =a-b;
		System.out.println(a+" - "+b+" = "+sub);

	}	
	
	public static void main(String [] args){
		
		Addition(12,13);
		Substraction(13,12);
		
		
	}
}