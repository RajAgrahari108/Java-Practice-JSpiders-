class Switch_Sta1{
	public static void main(String[] args){
		int choice = 2;
		switch(choice)
		{
			case 1 : System.out.println("COD");
			break;
			
			case 2 : System.out.println("UPI");
			break;
			
			case 3 : System.out.println("CARD");
			break;
			
			case 4 : System.out.println("NREBANKING");
			break;
			
			case 5 : System.out.println("CASH");
			break;

			default:System.out.println("CASH");
		}
	}
}