class Switch_Sta3{
	public static void main(String[] args){
		char ch='b';
			switch(ch){
				case 'A' : System.out.println("Apple");
				break;
				
				case 'B' : System.out.println("Ball");
				break;
				
				case 'C' : System.out.println("Cat");
				break;
				
				case 'D' : System.out.println("Dog");
				break;
				
				default: System.out.println("Under Development");
				
			}
	}
}